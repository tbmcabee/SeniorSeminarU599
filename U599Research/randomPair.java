package U599Research;

public class randomPair 
{
	int randOne;
	int randTwo;
	
	public randomPair(int r1, int r2)
	{
		randOne = r1;
		randTwo = r2;
	}

	public int getRandOne() 
	{
		return randOne;
	}

	public void setRandOne(int randOne) 
	{
		this.randOne = randOne;
	}

	public int getRandTwo() 
	{
		return randTwo;
	}

	public void setRandTwo(int randTwo) 
	{
		this.randTwo = randTwo;
	}

}
